#include <iostream>
#include <cmath>
using namespace std;
int main() {
	setlocale(LC_ALL, "RU");
	long double x1, y1, x2, y2, x3, y3;
	long double d12, d13, d23, cos1, cos2, cos3, ygR1, ygR2, ygR3, PI, ygG1, ygG2, ygG3, S1, S2, S3, h1, h2, h3, m1, m2, m3, b1, b2, b3, R, r, P, SR, Sr, CR, Cr;
	cout << "Введите координаты x1 и y1 первой вершины треугольника соответсвенно: " << endl;
	cin >> x1 >> y1;
	cout << "Введите координаты x2 и y2 второй вершины треугольника соответсвенно: " << endl;
	cin >> x2 >> y2;
	cout << "Введите координаты x3 и y3 третьей вершины треугольника соответсвенно: " << endl;
	cin >> x3 >> y3;
	d12 = sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));
	d23 = sqrt((x3 - x2) * (x3 - x2) + (y3 - y2) * (y3 - y2));
	d13 = sqrt((x3 - x1) * (x3 - x1) + (y3 - y1) * (y3 - y1));
	cos1 = (d12 * d12 + d13 * d13 - d23 * d23) / (2.0 * d12 * d13);
	cos2 = (d12 * d12 + d23 * d23 - d13 * d13) / (2.0 * d12 * d23);
	cos3 = (d23 * d23 + d13 * d13 - d12 * d12) / (2.0 * d23 * d13);
	ygR1 = acos(cos1);
	ygR2 = acos(cos2);
	ygR3 = acos(cos3);
    PI = acos(-1);
	ygG1 = (180.0 * ygR1) / PI;
	ygG2 = (180.0 * ygR2) / PI;
	ygG3 = (180.0 * ygR3) / PI;
	h1 = sin(ygR2) * d12;
	h2 = sin(ygR1) * d12;
	h3 = sin(ygR1) * d13;
	S1 = 0.5 * d12 * d13 * sin(ygR1);
	S2 = sqrt(((d12 + d13 + d23) / 2) * (((d12 + d13 + d23) / 2) - d12) * (((d12 + d13 + d23) / 2) - d23) * (((d12 + d13 + d23) / 2) - d13));
	S3 = 0.5 * h1 * d23;
	m1 = 0.5 * sqrt(2 * d12 * d12 + 2 * d13 * d13 - d23 * d23);
	m2 = 0.5 * sqrt(2 * d12 * d12 + 2 * d23 * d23 - d13 * d13);
	m3 = 0.5 * sqrt(2 * d23 * d23 + 2 * d13 * d13 - d12 * d12);
	b1 = (2.0 * S1) / (sin(ygR1 / 2.0) * (d12 + d13));
	b2 = (2.0 * S1) / (sin(ygR2 / 2.0) * (d12 + d23));
	b3 = (2.0 * S1) / (sin(ygR3 / 2.0) * (d23 + d13));
	R = d23 / (2.0 * sin(ygR1));
	P = d12 + d23 + d13;
	r = (2.0 * S1) / P;
	Sr = PI * r * r;
	SR = PI * R * R;
	CR = 2 * PI * R;
	Cr = 2 * PI * r;
	cout << "1. Длины сторон треугольника:\t\t" << d12 << "\t\t" << d23 << "\t\t" << d13 << endl;
	cout << "2. Высоты треугольника:\t\t" << h1 << "\t\t" << h2 << "\t\t" << h3 << endl;
	cout << "3. Медианы треугольника:\t\t" << m1 << "\t\t" << m2 << "\t\t" << m3 << endl;
	cout << "4. Биссектрисы треугольника:\t\t" << b1 << "\t\t" << b2 << "\t\t" << b3 << endl;
	cout << "5. Радианные меры углов треугольника:\t\t" << ygR1 << "\t\t" << ygR2 << "\t\t" << ygR3 << endl;
	cout << "Градусные меры углов треугольника:\t\t" << ygG1 << "°\t\t" << ygG2 << "°\t\t" << ygG3 << "°" << endl;
	cout << "6. Радиус вписаной окружности:\t\t" << r << endl;
	cout << "Радиус описаной окружности:\t\t" << R << endl;
	cout << "7. Длина описаной окружности:\t\t" << CR << endl;
	cout << "Длина вписаной окружности:\t\t" << Cr << endl;
	cout << "Площадь описаной окружности:\t\t" << SR << endl;
	cout << "Площадь вписаной окружности:\t\t" << Sr << endl;
	cout << "8. Площадь треугольника(3 способа):\t\t" << S1 << "\t\t" << S2 << "\t\t" << S3 << endl;
	cout << "Периметр треугольника:\t\t" << P << endl;
	return 0;
}